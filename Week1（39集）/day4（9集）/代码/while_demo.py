'''
 while: 关键字   完成循环

完整结构:

 while 条件:
 	语句体(块)
 else:
 	语句体(块)



'''
# 打印1-10之间的数字
# for i in range(1,11):
# 	print(i)

i=1
while i<=10:  # 10<=10 True   11<=10  False  结束循环结构
	print(i)  # print(10)
	i+=1  # i=11

print('打印完毕')


# 打印1-30之间的所有3的倍数
# 方式1：
n=1
while n<=30:
	# 进入循环体
	if n%3==0:
		print('---->',n)
	# 改变n
	n+=1

print('*'*30)
# 方式2:
n=3
while n<=30:
	print('====>',n)
	n+=3

print('*'*30)
# 方式3: (不推荐)

n=3
while n<=30 and n%3==0:
	print('********>',n)
	n+=3

# 打印1-30之间的所有既是3的倍数还是5的倍数
n=1

while n<=30:
	if n%3==0 and n%5==0:
		print('----------------->',n)

	# 改变n的值
	n+=1

'''

使用while循环计算1-20的累加和

1+2+3+4+...+20

'''
